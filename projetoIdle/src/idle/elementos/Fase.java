
package idle.elementos;

public class Fase {
    private String nome;
    private int nivel;
    private float fNivel;
    private int pontos_recompensa;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getNivel() {
        return nivel;
    }

    public void setNivel(int nivel) {
        this.nivel = nivel;
    }

    public float getfNivel() {
        return fNivel;
    }

    public void setfNivel(float fNivel) {
        this.fNivel = fNivel;
    }

    public int getPontos_recompensa() {
        return pontos_recompensa;
    }

    public void setPontos_recompensa(int pontos_recompensa) {
        this.pontos_recompensa = pontos_recompensa;
    }
    
}
